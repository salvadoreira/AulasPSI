using System;

namespace TiposVarios
{
    class Program
    {
        static void Main(string[] args)
        {
            //Variáveis Inteiras.
            int numero = 23;
            uint i = 4U;
            ulong d = 7UL;
            long c = 2UL;

            //Variáveis Reais.
            float n1 = 7f;
            double n2 = 22;
            decimal n3 = 44m;


            Console.WriteLine(numero);
            Console.WriteLine(i);
            Console.WriteLine(d);
            Console.WriteLine(c);
            Console.WriteLine(n1);
            Console.WriteLine(n2);
            Console.WriteLine(n3);
        }
    }
}
